public class Helper
{
    public static Helper[] helpers = new Helper[9];
    
    public float productivity;
    public int price;
    
    public Helper(float prod, int pr)
    {
        productivity = prod;
        price = pr;
    }
    
    public static void registerHelpers()
    {
        helpers[0] = new HelperClicker(0.1F, 15);
        helpers[1] = new HelperGrandma(0.5F, 100);
        helpers[2] = new HelperFarm(2F, 500);
        helpers[3] = new HelperFactory(10F, 3000);
        helpers[4] = new HelperMine(40F, 10000);
        helpers[5] = new HelperShipment(100F, 40000);
        helpers[6] = new HelperAlchemyLab(400F, 200000);
        helpers[7] = new HelperPortal(6666F, 1666666);
        helpers[8] = new HelperTimeMachine(98765F, 123456789);
    }
}
